#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	res;
	int	val;
	int	i;
	int	sign;

	res = 0;
	val = 0;
	i = 0;
	sign = 1;
	while (nptr[i] == 32 || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			sign = sign * -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		val = nptr[i] - '0';
		res = (res * 10) + val;
		i++;
	}
	return (res * sign);
}
